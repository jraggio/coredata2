    @IBAction func addPet(sender: AnyObject) {
        
        
        let alert = UIAlertController(title: "New Pet",
                                      message: "Add pet name",
                                      preferredStyle: .Alert)
        
        let saveAction = UIAlertAction(title: "Save",
                                       style: .Default,
                                       handler: { (action:UIAlertAction) -> Void in
                                        
                                        let textField = alert.textFields!.first
                                        self.savePet(textField!.text!)
                                        self.tableView.reloadData()
        })
        
        let cancelAction = UIAlertAction(title: "Cancel",
                                         style: .Default) { (action: UIAlertAction) -> Void in
        }
        
        alert.addTextFieldWithConfigurationHandler {
            (textField: UITextField) -> Void in
        }
        
        alert.addAction(saveAction)
        alert.addAction(cancelAction)
        
        presentViewController(alert,
                              animated: true,
                              completion: nil)

    }
    
    func savePet(name: String) {
        let appDelegate = UIApplication.sharedApplication().delegate as! AppDelegate
        let managedContext = appDelegate.managedObjectContext

        let entity = NSEntityDescription.entityForName("Pet", inManagedObjectContext: managedContext)
        let pet = Pet(entity: entity!,insertIntoManagedObjectContext: managedContext)
        pet.name = name
        
        //Insert the new Pet into the Person's set
        let pets = person?.owns?.mutableCopy() as! NSMutableOrderedSet
        pets.addObject(pet)
        
        person?.owns = pets.copy() as? NSOrderedSet

        do {
            try managedContext.save()
        } catch let error as NSError  {
            print("Could not save \(error), \(error.userInfo)")
        }
    }
    
    
    
